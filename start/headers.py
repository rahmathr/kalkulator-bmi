def header(title: str = "Kalkulator BMI") -> None:
    """
    Menampilkan header dengan teks yang dapat disesuaikan.

    Parameters:
    title (str): Teks judul untuk header. Default adalah 'Kalkulator BMI'.
    """
    print(30 * "=")
    print(f"{7 * '-'} {title} {7 * '-'}")
    print(30 * "=")
